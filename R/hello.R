#' Adds two numbers
#'
#' The function returns the sum of two numbers
#'
#' @param x A number.
#' @param y A number.
#'
#' @return The sum of \code{x} and \code{y}
#' @export
#'
#' @examples
#' sum_f(2,3)
sum_f <- function(x,y) {
  return(x+y)
}


#' Multiplies two numbers
#'
#'The function returns the product of two numbers
#'
#' @param x A number
#' @param y A number
#'
#' @return The product of \code{x} and \code{y}
#' @export
#'
#' @examples
#' product_f(2,3)
product_f <- function(x,y) {
  return(x*y)
}