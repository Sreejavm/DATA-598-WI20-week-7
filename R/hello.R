#' Adds two numbers
#'
#' The function returns the sum of two numbers
#'
#' @param x
#' @param y
#'
#' @return
#' @export
#'
#' @examples
#' sum_f(2,3)
sum_f <- function(x,y) {
  return(x+y)
}


#' Multiplies two numbers
#'
#'The function returns the product of two numbers
#'
#' @param x
#' @param y
#'
#' @return
#' @export
#'
#' @examples
#' product_f(2,3)
product_f <- function(x,y) {
  return(x*y)
}
